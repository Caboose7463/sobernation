import { notFound } from 'next/navigation'
import type { Metadata } from 'next'
import {  getLocationSlugs, getLocation , getTopLocationSlugs } from '../../../lib/locations'
import RehabTypePage from '../../../components/RehabTypePage'
import { PRIVATE_REHAB_CONFIG } from '../../../lib/rehab-types'

export const dynamicParams = true

export async function generateStaticParams() {
  return getTopLocationSlugs().map(location => ({ location }))
}

export async function generateMetadata(
  { params }: { params: Promise<{ location: string }> }
): Promise<Metadata> {
  const { location } = await params
  const loc = getLocation(location)
  if (!loc) return {}
  return {
    title: `Private Rehab in ${loc.name} | Private Addiction Treatment | SoberNation`,
    description: `Private residential rehab in ${loc.name} — same-week admission, medically supervised detox, and structured therapy. Costs from £3,000. NHS-funded places also available. Call 0300 123 6600.`,
  }
}

export default async function Page(
  { params }: { params: Promise<{ location: string }> }
) {
  const { location } = await params
  const loc = getLocation(location)
  if (!loc) notFound()
  return <RehabTypePage config={PRIVATE_REHAB_CONFIG} location={loc.name} locationSlug={location} loc={loc} />
}