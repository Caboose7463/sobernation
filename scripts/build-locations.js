/**
 * build-locations.js
 * Processes GeoNames GB.txt to produce a comprehensive list of
 * all UK towns and cities (population >= 1,000), then merges
 * with CQC rehab data to produce the final lib/locations.ts
 *
 * GeoNames columns (tab-separated):
 * 0:geonameid 1:name 2:asciiname 3:alternatenames 4:lat 5:lng
 * 6:featureClass 7:featureCode 8:countryCode 9:cc2
 * 10:admin1Code 11:admin2Code 12:admin3Code 13:admin4Code
 * 14:population 15:elevation 16:dem 17:timezone 18:modificationDate
 *
 * Feature codes we want (populated places):
 *   PPL  - populated place
 *   PPLA - seat of a first-order administrative division
 *   PPLA2 - seat of a second-order administrative division
 *   PPLA3, PPLA4, PPLC (capital), PPLX (section of populated place)
 */

const fs   = require('fs')
const path = require('path')

const GEO_PATH   = path.join(__dirname, '../data/geonames/GB.txt')
const REHAB_PATH = path.join(__dirname, '../data/rehabs.json')
const OUT_TS     = path.join(__dirname, '../lib/locations.ts')
const OUT_JSON   = path.join(__dirname, '../data/locations.json')

// Admin1 codes for GB → country name
const ADMIN1_COUNTRY = {
  ENG: 'England',
  SCT: 'Scotland',
  WLS: 'Wales',
  NIR: 'Northern Ireland',
}

function slugify(str) {
  return str.toLowerCase()
    .replace(/[^a-z0-9\s]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim()
}

// Feature codes for populated places
const PLACE_CODES = new Set([
  'PPL','PPLA','PPLA2','PPLA3','PPLA4','PPLC','PPLS','PPLX','PPLF','PPLG'
])

console.log('Reading GeoNames GB.txt...')
const lines = fs.readFileSync(GEO_PATH, 'utf8').split('\n')
console.log(`Total rows: ${lines.length}`)

const MIN_POPULATION = 1000

const places = []

for (const line of lines) {
  if (!line.trim()) continue
  const cols = line.split('\t')
  if (cols.length < 15) continue

  const featureClass = cols[6]
  const featureCode  = cols[7]
  const population   = parseInt(cols[14], 10) || 0
  const admin1       = cols[10]  // ENG / SCT / WLS / NIR / GB

  if (featureClass !== 'P') continue            // only populated places
  if (!PLACE_CODES.has(featureCode)) continue   // right feature codes
  if (population < MIN_POPULATION) continue     // min population threshold

  const name    = cols[1]
  const lat     = parseFloat(cols[4])
  const lng     = parseFloat(cols[5])
  const admin2  = cols[11]  // county code
  const country = ADMIN1_COUNTRY[admin1] || 'England'
  const slug    = slugify(name)

  if (!slug) continue

  places.push({ slug, name, country, admin1, admin2, lat, lng, population })
}

// Deduplicate by slug (keep highest population)
const bySlug = {}
for (const p of places) {
  if (!bySlug[p.slug] || bySlug[p.slug].population < p.population) {
    bySlug[p.slug] = p
  }
}

const unique = Object.values(bySlug).sort((a, b) => b.population - a.population)
console.log(`\nUnique towns >= ${MIN_POPULATION} pop: ${unique.length}`)

// Country breakdown
const countryCounts = {}
for (const p of unique) { countryCounts[p.country] = (countryCounts[p.country] || 0) + 1 }
console.log('By country:', countryCounts)

// Largest places
console.log('\nTop 20 by population:')
unique.slice(0, 20).forEach(p => console.log(`  ${p.name} (${p.country}): ${p.population.toLocaleString()}`))

// Load rehab data
console.log('\nLoading rehab data...')
const rehabData = JSON.parse(fs.readFileSync(REHAB_PATH, 'utf8'))
const rehabTowns = new Set(Object.keys(rehabData.byTown))
let withRehabs = 0
for (const p of unique) {
  if (rehabTowns.has(p.slug)) withRehabs++
}
console.log(`Towns with real CQC rehab data: ${withRehabs} / ${unique.length}`)

// ── Write locations.json ──────────────────────────────────────────────────────
console.log('\nWriting locations.json...')
fs.writeFileSync(OUT_JSON, JSON.stringify({ total: unique.length, locations: unique }, null, 2))

// ── Write lib/locations.ts ────────────────────────────────────────────────────
console.log('Writing lib/locations.ts...')

const tsContent = `// AUTO-GENERATED by scripts/build-locations.js
// Source: GeoNames GB dataset (Creative Commons Attribution licence)
// Do not edit manually — run scripts/build-locations.js to regenerate

export interface UKLocation {
  slug: string
  name: string
  country: 'England' | 'Scotland' | 'Wales' | 'Northern Ireland'
  admin1: string
  admin2: string
  lat: number
  lng: number
  population: number
}

export const LOCATIONS: UKLocation[] = ${JSON.stringify(unique, null, 2)}

// Lookup by slug (O(1))
const LOCATION_MAP: Record<string, UKLocation> = Object.fromEntries(
  LOCATIONS.map(l => [l.slug, l])
)

export function getLocation(slug: string): UKLocation | null {
  return LOCATION_MAP[slug] ?? null
}

export function getLocationSlugs(): string[] {
  return LOCATIONS.map(l => l.slug)
}
`

fs.writeFileSync(OUT_TS, tsContent)
const kb = Math.round(fs.statSync(OUT_TS).size / 1024)
console.log(`Saved lib/locations.ts (${kb} KB, ${unique.length} locations)`)
